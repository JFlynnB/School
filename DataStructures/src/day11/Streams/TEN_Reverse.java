package day11.Streams;

/*
 * 10.Reverse the string
 */
public class TEN_Reverse {

	public static void main(String[] args) {
		
		String input = new String("Quick, easy, and secure.");
		StringBuilder result = new StringBuilder();
		
		input.chars().mapToObj(c -> (char) c).forEach(c -> result.insert(0, c));
		
		System.out.println(result.toString());
		
		
	}

}
